def get_cmap(num_categories, mode="light"):
    cmap = [gray_light] if mode == "light" else [gray_dark]
    glasbey = glasbey_dark if mode == "light" else glasbey_light
    for i in range(ceil(num_categories / len(glasbey)) + 1):
        cmap += glasbey
    return cmap


